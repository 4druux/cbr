    <div class="panel panel-primary">
    <div class="panel-heading">
        <h3 class="panel-title">Tips Untuk Orang Tua Dalam Memenuhi Gizi Balita</h3>
	</div>
	<div class="panel-body">
    <p style="text-align:justify;" >
<br>
Memastikan balita mendapat gizi yang cukup adalah kunci tumbuh kembang optimal. 
Untuk bayi 0–6 bulan, ASI eksklusif adalah sumber nutrisi terbaik karena mengandung antibodi dan zat gizi lengkap.
 Ibu menyusui disarankan mengonsumsi makanan bergizi seperti sayuran, protein, dan buah untuk meningkatkan kualitas ASI.
  Setelah 6 bulan, bayi perlu diperkenalkan dengan MPASI (Makanan Pendamping ASI) secara bertahap, dimulai dari tekstur halus seperti bubur, lalu meningkat ke makanan yang lebih padat seiring pertumbuhan gigi dan kemampuan mengunyah. 
  Frekuensi makan juga perlu disesuaikan, misalnya 2–3 kali makan utama dan 1–2 kali camilan sehat per hari.
  </br>
</br>
Prioritaskan makanan kaya protein, zat besi, vitamin A, dan kalsium seperti telur, ikan, hati ayam, tempe, wortel, dan susu. 
Hindari gula dan garam berlebihan agar anak tidak terbiasa dengan rasa terlalu manis atau asin. 
Jika anak sulit makan, coba variasikan menu dan sajian, misalnya dengan membentuk nasi menjadi karakter lucu atau menyajikan sayur dalam bentuk sup. 
Jangan memaksa anak makan, tetapi tawarkan kembali makanan yang sebelumnya ditolak dengan cara berbeda.
<br>
</br>
Pantau pertumbuhan anak secara rutin di Posyandu atau Puskesmas untuk memastikan berat dan tinggi badan sesuai grafik KMS (Kartu Menuju Sehat). 
Jika berat badan tidak naik dalam dua bulan berturut-turut, segera konsultasikan ke tenaga kesehatan.
 Selain asupan makanan, kebersihan juga penting—pastikan anak selalu cuci tangan sebelum makan dan setelah bermain untuk mencegah infeksi yang dapat 
 mengganggu penyerapan nutrisi.
<br>
</br>
Hindari mitos keliru seperti "anak gemuk berarti sehat" atau "ASI tidak cukup setelah usia 1 tahun". 
Faktanya, obesitas pada balita bisa memicu masalah kesehatan, dan ASI tetap bermanfaat meski sudah ada MPASI. 
Jika membutuhkan bantuan, manfaatkan layanan Posyandu, aplikasi seperti PrimaKu dari Kemenkes, atau konsultasi online dengan ahli gizi.
 Dengan pola asuh dan pemantauan yang tepat, orang tua dapat mencegah masalah gizi seperti stunting, wasting, atau obesitas sejak dini.
<br>
</p>
