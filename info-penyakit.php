        <h3 class="panel-title">Tentang Status Gizi Balita</h3>
	</div>
	<div class="panel-body">
	<p style="text-align:justify;">
	Status gizi balita merupakan tolok ukur penting dalam memantau tumbuh kembang anak. 
    Masa balita merupakan periode emas dimana otak dan fisik berkembang pesat, sehingga kebutuhan gizi yang optimal mutlak diperlukan. 
    Di sisi lain, gizi yang tidak mencukupi atau tidak seimbang dapat berakibat serius seperti stunting, wasting, obesitas, bahkan meningkatkan risiko kematian.
     Untuk itu, pemahaman orang tua dan kewaspadaan tenaga kesehatan terhadap tanda-tanda gangguan gizi sejak dini sangat krusial guna mencegah dampak jangka panjang yang merugikan.</br>
<br> Mengapa Pemahaman Status Gizi Balita Sangat Penting?
<br>- Gizi buruk (Malnutrisi Berat) masih menjadi penyebab utama kematian balita di negara berkembang</br>
- Stunting (tubuh pendek akibat kekurangan gizi kronis) berdampak negatif pada perkembangan kognitif dan menurunkan produktivitas saat dewasa</br>
- Obesitas pada balita meningkatkan risiko diabetes dan penyakit jantung di masa depan</br>

<br>Berdasarkan temuan Riset Kesehatan Dasar (Riskesdas) tahun 2018, Indonesia masih menghadapi tantangan besar dalam bidang kesehatan gizi balita.
 Data yang cukup mengkhawatirkan menunjukkan bahwa angka prevalensi stunting atau kondisi gagal tumbuh pada anak akibat kekurangan gizi kronis mencapai 30,8% dari total populasi balita. 
 Selain itu, masalah wasting atau gizi akut yang ditandai dengan berat badan sangat rendah dibanding tinggi badan juga terlihat cukup signifikan, yaitu sebesar 10,2%.

Angka-angka statistik ini bukan sekadar data biasa, melainkan merupakan cerminan nyata dari kondisi kesehatan anak-anak Indonesia yang masih memerlukan perhatian serius dari berbagai pihak.
 Tingginya persentase kasus stunting dan wasting ini menunjukkan bahwa upaya perbaikan gizi balita di tanah air masih menghadapi berbagai kendala dan tantangan yang kompleks. </p>	
